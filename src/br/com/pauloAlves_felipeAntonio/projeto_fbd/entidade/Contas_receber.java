public class Contas_receber {
	private int id,id_caixas,id_vendas ;
	private float valor;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId_caixas() {
		return id_caixas;
	}
	public void setId_caixas(int id_caixas) {
		this.id_caixas = id_caixas;
	}
	public int getId_vendas() {
		return id_vendas;
	}
	public void setId_vendas(int id_vendas) {
		this.id_vendas = id_vendas;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	
	

}
